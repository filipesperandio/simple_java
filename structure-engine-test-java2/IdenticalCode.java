public class IdenticalCode {
  public static void isPrime(int number) {
    boolean isPrime = true;

    for(int i=2; i<=number /2;i++) {
      temp = num % i;

      if (temp==0) {
        isPrime = false;
        break;
      }
    }

    if (isPrime) {
      System.out.println(num + " is Prime Number");
    } else {
      System.out.println(num + " is not Prime Number");
    }
  }
}

